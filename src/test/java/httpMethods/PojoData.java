package httpMethods;

public class PojoData 
{
	/*
	 * POJO = plain old java Object
	 * Encapsulation=private data+ public function
	 */
	
	private String name;
	private String job;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	
	
	
}
